/*7. Number Eight: Write addition, subtraction, multiplication, and division operations
that each result in the number 8. Be sure to enclose your operations in print statements to see the results.*/

//Addition Operation
function sum(num1: number, num2: number) {
  let c = num1 + num2;
  return c;
}
console.log(sum(5, 3));

//Subtraction Operation
function subtract(num1: number, num2: number) {
  let c = num1 - num2;
  return c;
}
console.log(subtract(11, 3));

//Multiplication Operation
function multiply(num1: number, num2: number) {
  let c = num1 * num2;
  return c;
}
console.log(multiply(4, 2));

//Division Operation
function division(num1: number, num2: number) {
  let c = num1 / num2;
  return c;
}
console.log(division(16, 2));
